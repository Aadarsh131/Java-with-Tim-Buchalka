package first_steps;
public class floatdouble {
    public static void main(String[] args){
        float a= 5.63f; float aa= 5f/3f;
        double b= 3.53d; double bb= 5d/3d;
        
        System.out.println(a);
        System.out.println(b);
        System.out.println(aa);
        System.out.println(bb);
    }
}
